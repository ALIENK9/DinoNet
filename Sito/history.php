</div>

<!-- L'estinzione -->

<div id="estinzione" class="green-sea padding-6">
	<div class="content padding-large">
		<h1> L'estinzione </h1>
		<hr>
		<p>
			<strong>L'estinzione dei dinosauri</strong> è un mistero che da sempre interessa <strong>paleontologi</strong> (coloro che studiano gli animali vissuti
			sulla terra prima degli attuali), studiosi e semplici appassionati. Al riguardo <strong>esistono molte teorie</strong>:
		</p>
		<ul>
			<li>
				<h3>La teoria del meteorite</h3>
				<p>
					Ogni giorno sulla Terra cadono  fino a 500 meteoriti  della grandezza di una pallina da tennis, eppure non ce ne
					accorgiamo. Come ne cadono di piccoli tutti giorni, così ne cadono di immensi,  molto raramente.
					Secondo alcuni studiosi, <strong>un meteorite di 10 chilometri</strong> si sarebbe schiantato sul suolo terrestre circa 65
					milioni di anni fa nella penisola dello  Yucatan, in Messico, creando un cratere di ben  180 chilometri di 
					diametro! Questo scontro terrificante avrebbe provocato una tremenda esplosione che avrebbe riempito l'aria di
					polvere e sollevato impenetrabili nubi. La Terra, per via di queste, non avrebbe visto la luce del Sole per mesi
					(alcuni ipotizzano per anni), e perciò il clima sarebbe cambiato repentinamente. Un gelido inverno avrebbe 
					ricoperto il suolo terrestre uccidendo moltissime specie di piante, estinguendo i dinosauri erbivori (che non
					trovarono più cibo per nutrirsi) e, di conseguenza, anche i carnivori, che se ne cibavano.
				</p>
			</li>
			<li>
				<h3>Le eruzioni vulcaniche</h3>
				<p>
					Recentemente, però, la teoria del meteorite è stata messa in discussione. Infatti, ricerche recenti affermano che
					in realtà il meteorite si schiantò sul nostro pianeta ben 300 mila anni prima dell'estinzione dei dinosauri, dunque
					non può essere ritenuto colpevole. Ecco allora che è stata elaborata una diversa teoria. Essa afferma che, sulla
					Terra, vi fu un <strong>periodo di grandi sconvolgimenti causati da terremoti continui</strong>. A questi si aggiunsero imponenti
					<strong>eruzioni vulcaniche</strong> che, liberando nell'atmosfera milioni di tonnellate di sostanze chimiche e ceneri,  resero 
					l'aria tossica e oscurarono il sole, creando un terrificante effetto serra (in pratica, la nostra Terra non 
					riusciva più a disperdere nello spazio l'eccesso di calore ricevuto dal Sole) che cambiò in modo radicale il
					clima e le stagioni. L'insieme di questi fatti portò,  nel giro di poco tempo, all'estinzione dei dinosauri.
				</p>
			</li>
			<li>
				<h3>Un disastro...combinato?</h3>
				<p>
					Una recente teoria <strong>metterebbe in relazione la prima teoria (meteorite) con l'ipotesi delle eruzioni</strong>.
					Alcuni ricercatori hanno infatti riscontrato importanti variazioni di 
					temperatura avvenuta sulla Terra proprio durante il periodo in cui scomparvero i dinosauri. Ciò sarebbe 
					riconducibile al fatto che i due disastri avvennero l'uno dopo l'altro, a circa 150.000 anni di distanza, 
					provocando cambiamenti climatici così radicali da impedire ai lucertoloni di adattarsi.
				</p>
			</li>
			<li>
				<h3>Le malattie e la crisi esistenziale</h3>
				<p>
					Alcuni studiosi sostengono che un'<strong>epidemia virale o batterica</strong> avrebbe portato all'estinzione di questi animali 
					giganteschi. Un gruppetto di altri studiosi sostiene invece che la colpa dell'estinzione più famosa della storia
					sia colpa dei primi piccoli mammiferi, che cominciarono a cibarsi delle uova di dinosauro.
				</p>
			</li>
			<li>
				<h3>Il normale corso della natura</h3>
				<p>
					Quella chiamata <strong>"del normale corso della Natura"</strong> è l'ultima teoria che avanzata nel corso degli
					anni. Essa afferma che l'estinzione dei dinosauri non è da ascrivere a nessun fattore in particolare ma al
					semplice decorso della Natura. Pensiamo infatti ad animali come il mammut o la tigre dai denti a sciabola: 
					si sono estinti in modo naturale, non a causa di alcun grande fattore ambientale o climatico.
					Come loro, anche oggi molti animali rischiano di estinguersi: pensate alla balenottera azzurra, che è molto più
					grande dei dinosauri e, di questo passo, fra poche centinaia di anni non ci sarà più! Pensiamo all'uccello dodo,
					sterminato dagli uomini appena 300 anni fa. Come vediamo, ai normali cambiamenti climatici si sono aggiunti i
					disastri causati dall'uomo, come la caccia e lo sfruttamento indiscriminato delle risorse naturali, vita compresa.
				</p>
			</li>	
		</ul>
	</div>
